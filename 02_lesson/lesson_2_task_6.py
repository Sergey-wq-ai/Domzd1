# Данный список
lst = [11, 5, 8, 32, 15, 3, 20, 132, 21, 4, 555, 9, 20]

# Находим элементы, которые одновременно:
# 1. Меньше 30
# 2. Делятся на 3 без остатка
result = [x for x in lst if x < 30 and x % 3 == 0]

# Выводим результат
print("Элементы, которые меньше 30 и делятся на 3 без остатка:")
print(result)

# Альтернативный вариант с подробным выводом
print("\nПодробный разбор:")
for num in lst:
    if num < 30 and num % 3 == 0:
        print(f"{num} - меньше 30 и делится на 3")